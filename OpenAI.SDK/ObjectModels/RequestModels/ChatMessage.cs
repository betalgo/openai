using System.Text.Json.Serialization;

namespace OpenAI.ObjectModels.RequestModels;

/// <summary>
///     The contents of the message.
///     Messages must be an array of message objects, where each object has a role (either “system”, “user”, or
///     “assistant”) and content (the content of the message) and an optional name
/// </summary>
public class ChatMessage
{
    /// <summary>
    /// </summary>
    /// <param name="role">The role of the author of this message. One of system, user,  assistant, or tool.</param>
    /// <param name="content">The contents of the message.</param>
    /// <param name="name">
    ///     The name of the author of this message. May contain a-z, A-Z, 0-9, and underscores, with a maximum
    ///     length of 64 characters.
    /// </param>
    /// <param name="functionCall">Deprecated and replaced by tool_calls.The name and arguments of a function that should be called, as generated by the model.</param>
    /// <param name="toolCalls">The tool calls generated by the model, such as function calls.</param>
    /// <param name="toolCallId">Tool call that this message is responding to.</param>
    public ChatMessage(string role, string content, string? name = null, FunctionCall? functionCall = null, IList<ToolCall>? toolCalls = null, string? toolCallId = null)
    {
        Role = role;
        Content = content;
        Name = name;
        FunctionCall = functionCall;
        ToolCalls = toolCalls;
        ToolCallId = toolCallId;
    }


    /// <summary>
    ///     The role of the author of this message. One of system, user, or assistant.
    /// </summary>
    [JsonPropertyName("role")]
    public string Role { get; set; }

    /// <summary>
    ///     The contents of the message.
    /// </summary>
    [JsonPropertyName("content")]
    public string Content { get; set; }

    /// <summary>
    ///     The name of the author of this message. May contain a-z, A-Z, 0-9, and underscores, with a maximum length of 64
    ///     characters.
    /// </summary>
    [JsonPropertyName("name")]
    public string? Name { get; set; }

    /// <summary>
    /// The tool calls generated by the model, such as function calls.
    /// </summary>
    [JsonPropertyName("tool_calls")]
    public IList<ToolCall>? ToolCalls { get; set; }

    /// <summary>
    ///     Deprecated and replaced by tool_calls.The name and arguments of a function that should be called, as generated by the model.
    /// </summary>
    [JsonPropertyName("function_call")]
    [Obsolete(" Deprecated and replaced by tool_calls.")]
    public FunctionCall? FunctionCall { get; set; }

    /// <summary>
    /// Tool call that this message is responding to.
    /// </summary>
    [JsonPropertyName("tool_call_id")]
    public string? ToolCallId { get; set; }



    public static ChatMessage FromAssistant(string content, string? name = null, FunctionCall? functionCall = null, IList<ToolCall>? toolCalls = null)
    {
        return new ChatMessage(StaticValues.ChatMessageRoles.Assistant, content, name, functionCall, toolCalls);
    }
    public static ChatMessage FromTool(string content, string? toolCallId = null)
    {
        return new ChatMessage(StaticValues.ChatMessageRoles.Function, content, toolCallId: toolCallId);
    }

    public static ChatMessage FromFunction(string content, string? name = null)
    {
        return new ChatMessage(StaticValues.ChatMessageRoles.Function, content, name);
    }

    public static ChatMessage FromUser(string content, string? name = null)
    {
        return new ChatMessage(StaticValues.ChatMessageRoles.User, content, name);
    }

    public static ChatMessage FromSystem(string content, string? name = null)
    {
        return new ChatMessage(StaticValues.ChatMessageRoles.System, content, name);
    }
}